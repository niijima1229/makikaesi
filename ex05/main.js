const dict = [
  { num: 0, text: "zero" },
  { num: 1, text: "one" },
  { num: 2, text: "two" },
  { num: 3, text: "three" },
  { num: 4, text: "four" },
  { num: 5, text: "five" },
  { num: 6, text: "six" },
  { num: 7, text: "seven" },
  { num: 8, text: "eight" },
  { num: 9, text: "nine" },
  { num: 10, text: "ten" },
];

const example = [5, -8, 10, 0 - 10, -1];

function showDict() {
  example.forEach((num) => myfunc(num));
}

function myfunc(num) {
  let text = "";
  dict.forEach((element) => {
    if (num === element.num) {
      text = element.text;
    }
  });
  console.log(`${num < 0 ? "minus " : ""}${text}`);
}

showDict();
